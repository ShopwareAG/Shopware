<?php

namespace Shopware\Struct;

/**
 * @package Shopware\Struct
 */
class Shop extends Base
{
    private $id;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param mixed $id
     * @return $this
     */
    public function setId($id)
    {
        $this->id = $id;
        return $this;
    }


}